-- ============================================================
-- Викторина: 10 тематических пулов по 30 вопросов, прогрессия
-- сложности и серверный таймер на ответ.
--
-- ПРАВИЛА ВЫДАЧИ ВОПРОСА (get_random_quiz_question):
--   - 1-й и 2-й вопрос гостя за всю историю (по факту ОТВЕТА,
--     независимо от исхода) — всегда 10 баллов, без таймера.
--   - Дальше, пока накопленных баллов < 50 — случайно 10/15/20.
--   - Как только баллов >= 50 — только 15/20, десятибалльные больше
--     не выдаются.
--   - Тема вопроса всегда случайна, вне зависимости от сложности.
--
-- ТАЙМЕР: начиная с 3-го вопроса — 20 секунд с момента выдачи.
-- Проверяется на сервере (issued_at против now() при ответе), а не
-- только в интерфейсе — иначе это чисто декоративная защита.
--
-- АРХИТЕКТУРНЫЙ СДВИГ: раньше quiz_attempts писалась только один
-- раз, в момент ответа, и только если баллы реально начислены.
-- Теперь строка создаётся СРАЗУ при выдаче вопроса (issued_at,
-- is_correct=NULL) — это единственный способ честно: 1) не повторять
-- вопрос, 2) знать, сколько вопросов гость уже видел, 3) проверить
-- таймер. check_quiz_answer обновляет эту же строку. Начисление
-- баллов (credit_quiz_points / register_guest_and_credit_quiz)
-- обновляет её ещё раз — points_awarded защищён условием =0, чтобы
-- баллы не начислялись дважды.
--
-- Запускать из-под supabase_admin:
--   sudo docker exec -i supabase-db psql -U supabase_admin -d postgres \
--     -v ON_ERROR_STOP=1 < supabase/migrations/20260713020000_quiz_topics_difficulty_timer.sql
-- ============================================================

BEGIN;

-- ------------------------------------------------------------
-- Схема: тема + строгая шкала баллов
-- ------------------------------------------------------------
ALTER TABLE public.quiz_questions ADD COLUMN IF NOT EXISTS topic text;

DO $do$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'quiz_questions_points_check') THEN
    ALTER TABLE public.quiz_questions ADD CONSTRAINT quiz_questions_points_check
      CHECK (points IN (10, 15, 20));
  END IF;
END $do$;

CREATE INDEX IF NOT EXISTS quiz_questions_topic_points_idx
  ON public.quiz_questions (topic, points) WHERE is_active = true;

-- ------------------------------------------------------------
-- Схема: issued_at/answered_at для честной прогрессии и таймера.
-- Существующие строки (тестовые данные) получают issued_at=answered_at
-- = их прежний created_at, чтобы не терять историю подчистую.
-- ------------------------------------------------------------
ALTER TABLE public.quiz_attempts ALTER COLUMN is_correct DROP NOT NULL;
ALTER TABLE public.quiz_attempts ADD COLUMN IF NOT EXISTS issued_at timestamptz;
ALTER TABLE public.quiz_attempts ADD COLUMN IF NOT EXISTS answered_at timestamptz;

UPDATE public.quiz_attempts SET issued_at = created_at WHERE issued_at IS NULL;
UPDATE public.quiz_attempts SET answered_at = created_at WHERE answered_at IS NULL;

ALTER TABLE public.quiz_attempts ALTER COLUMN issued_at SET NOT NULL;
ALTER TABLE public.quiz_attempts ALTER COLUMN issued_at SET DEFAULT now();

-- ------------------------------------------------------------
-- Выдача вопроса: прогрессия сложности + фиксация issued_at.
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.get_random_quiz_question(p_guest_id bigint)
RETURNS TABLE(question_id uuid, question text, options jsonb, points integer, time_limit_seconds integer)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_answered_count integer;
  v_current_points integer;
  v_allowed_points integer[];
  v_time_limit     integer;
  v_question_id    uuid;
BEGIN
  SELECT COUNT(*) INTO v_answered_count
  FROM public.quiz_attempts
  WHERE guest_id = p_guest_id AND answered_at IS NOT NULL;

  SELECT g.points INTO v_current_points FROM public.guests g WHERE g.id = p_guest_id;
  v_current_points := COALESCE(v_current_points, 0);

  IF v_answered_count < 2 THEN
    v_allowed_points := ARRAY[10];
    v_time_limit := NULL;
  ELSIF v_current_points >= 50 THEN
    v_allowed_points := ARRAY[15, 20];
    v_time_limit := 20;
  ELSE
    v_allowed_points := ARRAY[10, 15, 20];
    v_time_limit := 20;
  END IF;

  SELECT qq.id INTO v_question_id
  FROM public.quiz_questions qq
  WHERE qq.is_active = true
    AND qq.points = ANY(v_allowed_points)
    AND NOT EXISTS (
      SELECT 1 FROM public.quiz_attempts qa
      WHERE qa.guest_id = p_guest_id AND qa.question_id = qq.id
    )
  ORDER BY random()
  LIMIT 1;

  IF v_question_id IS NULL THEN
    -- Все подходящие по сложности вопросы исчерпаны — допускаем
    -- повтор, но остаёмся в рамках той же допустимой сложности.
    SELECT qq.id INTO v_question_id
    FROM public.quiz_questions qq
    WHERE qq.is_active = true
      AND qq.points = ANY(v_allowed_points)
    ORDER BY random()
    LIMIT 1;
  END IF;

  IF v_question_id IS NULL THEN
    RETURN;
  END IF;

  INSERT INTO public.quiz_attempts (guest_id, question_id, issued_at, is_correct, answered_at, points_awarded)
  VALUES (p_guest_id, v_question_id, now(), NULL, NULL, 0)
  ON CONFLICT (guest_id, question_id) DO UPDATE
    SET issued_at = now(), answered_at = NULL, is_correct = NULL, points_awarded = 0
    WHERE quiz_attempts.answered_at IS NOT NULL;

  RETURN QUERY
    SELECT qq.id, qq.question, qq.options, qq.points, v_time_limit
    FROM public.quiz_questions qq
    WHERE qq.id = v_question_id;
END;
$$;

GRANT EXECUTE ON FUNCTION public.get_random_quiz_question(bigint) TO anon, authenticated, service_role;

-- ------------------------------------------------------------
-- Проверка ответа: логирует попытку СРАЗУ (не только когда верно),
-- принудительно засчитывает как неверный ответ после истечения
-- таймера (не доверяя клиентскому отсчёту).
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.check_quiz_answer(
  p_guest_id bigint,
  p_question_id uuid,
  p_selected_index integer
)
RETURNS TABLE(is_correct boolean, correct_text text, is_timeout boolean)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_correct_index         integer;
  v_correct_text          text;
  v_is_correct            boolean;
  v_issued_at             timestamptz;
  v_answered_before_count integer;
  v_is_timeout            boolean := false;
BEGIN
  SELECT qq.correct_index, qq.options ->> qq.correct_index
  INTO v_correct_index, v_correct_text
  FROM public.quiz_questions AS qq WHERE qq.id = p_question_id;

  v_is_correct := (v_correct_index IS NOT NULL AND v_correct_index = p_selected_index);

  SELECT qa.issued_at INTO v_issued_at
  FROM public.quiz_attempts qa
  WHERE qa.guest_id = p_guest_id AND qa.question_id = p_question_id;

  SELECT COUNT(*) INTO v_answered_before_count
  FROM public.quiz_attempts
  WHERE guest_id = p_guest_id AND answered_at IS NOT NULL;

  IF v_answered_before_count >= 2 AND v_issued_at IS NOT NULL
     AND now() - v_issued_at > interval '20 seconds' THEN
    v_is_timeout := true;
    v_is_correct := false;
  END IF;

  UPDATE public.quiz_attempts
  SET is_correct = v_is_correct,
      answered_at = now()
  WHERE guest_id = p_guest_id AND question_id = p_question_id AND answered_at IS NULL;

  RETURN QUERY SELECT v_is_correct, v_correct_text, v_is_timeout;
END;
$$;

GRANT EXECUTE ON FUNCTION public.check_quiz_answer(bigint, uuid, integer) TO anon, authenticated, service_role;

-- ------------------------------------------------------------
-- Начисление баллов уже зарегистрированному гостю (без SMS).
-- quiz_attempts-строка уже существует (создана при выдаче, обновлена
-- при ответе через check_quiz_answer) — здесь только доначисляем
-- points_awarded, защищено условием =0 от повторного начисления.
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.credit_quiz_points(
  p_device_id text,
  p_question_id uuid,
  p_selected_index integer
)
RETURNS TABLE(ok boolean, points integer)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_guest_id        bigint;
  v_correct_index   integer;
  v_question_points integer;
  v_is_correct      boolean;
  v_row_count       integer;
BEGIN
  SELECT g.id INTO v_guest_id
  FROM public.guests AS g
  WHERE g.device_id = p_device_id AND g.phone IS NOT NULL;

  IF v_guest_id IS NULL THEN
    RETURN QUERY SELECT false, 0;
    RETURN;
  END IF;

  SELECT qq.correct_index, qq.points INTO v_correct_index, v_question_points
  FROM public.quiz_questions AS qq WHERE qq.id = p_question_id;

  v_is_correct := (v_correct_index IS NOT NULL AND v_correct_index = p_selected_index);

  IF v_is_correct THEN
    UPDATE public.quiz_attempts
    SET points_awarded = v_question_points, is_correct = true
    WHERE guest_id = v_guest_id AND question_id = p_question_id AND points_awarded = 0;

    GET DIAGNOSTICS v_row_count = ROW_COUNT;

    IF v_row_count > 0 THEN
      UPDATE public.guests AS g SET points = g.points + v_question_points WHERE g.id = v_guest_id;
    END IF;
  END IF;

  RETURN QUERY SELECT true, (SELECT g.points FROM public.guests AS g WHERE g.id = v_guest_id);
END;
$$;

GRANT EXECUTE ON FUNCTION public.credit_quiz_points(text, uuid, integer) TO anon, authenticated, service_role;

-- ------------------------------------------------------------
-- Регистрация + начисление баллов (первый раз, через SMS). Та же
-- UPDATE-логика, что и credit_quiz_points — quiz_attempts-строка уже
-- есть от check_quiz_answer.
-- ------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.register_guest_and_credit_quiz(
  p_device_id text,
  p_name text,
  p_phone text,
  p_question_id uuid DEFAULT NULL,
  p_selected_index integer DEFAULT NULL
)
RETURNS TABLE(ok boolean, phone_taken boolean, points integer)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_guest_id        bigint;
  v_correct_index   integer;
  v_question_points integer;
  v_is_correct      boolean := false;
  v_row_count       integer;
BEGIN
  BEGIN
    UPDATE public.guests AS g
    SET name = p_name, phone = p_phone
    WHERE g.device_id = p_device_id
    RETURNING g.id INTO v_guest_id;
  EXCEPTION WHEN unique_violation THEN
    RETURN QUERY SELECT false, true, 0;
    RETURN;
  END;

  IF v_guest_id IS NULL THEN
    RETURN QUERY SELECT false, false, 0;
    RETURN;
  END IF;

  IF p_question_id IS NOT NULL THEN
    SELECT qq.correct_index, qq.points INTO v_correct_index, v_question_points
    FROM public.quiz_questions AS qq WHERE qq.id = p_question_id;

    v_is_correct := (v_correct_index IS NOT NULL AND v_correct_index = p_selected_index);

    IF v_is_correct THEN
      UPDATE public.quiz_attempts
      SET points_awarded = v_question_points, is_correct = true
      WHERE guest_id = v_guest_id AND question_id = p_question_id AND points_awarded = 0;

      GET DIAGNOSTICS v_row_count = ROW_COUNT;

      IF v_row_count > 0 THEN
        UPDATE public.guests AS g SET points = g.points + v_question_points WHERE g.id = v_guest_id;
      END IF;
    END IF;
  END IF;

  RETURN QUERY SELECT true, false, (SELECT g.points FROM public.guests AS g WHERE g.id = v_guest_id);
END;
$$;

GRANT EXECUTE ON FUNCTION public.register_guest_and_credit_quiz(text, text, text, uuid, integer) TO service_role;

