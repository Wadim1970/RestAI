-- ============================================================
-- ФИКС: неоднозначность перегрузки call_waiter (PostgREST PGRST203),
-- из-за которой рвётся вся связь гость↔официант.
--
-- Предыстория. Первая миграция (20260707020000_waiter_calls.sql) создала
--   call_waiter(p_restaurant_id uuid, p_table_number text)      -- 2 аргумента
-- Позже 20260716010000_call_waiter_reason.sql добавила p_reason и сделала
--   CREATE OR REPLACE FUNCTION call_waiter(uuid, text, text DEFAULT NULL)
-- в ошибочном расчёте (см. комментарий в той миграции), что дефолтный
-- параметр «заменяет функцию на месте». На деле идентичность функции в
-- Postgres — это ИМЯ + ТИПЫ аргументов. (uuid, text) и (uuid, text, text)
-- — разные сигнатуры, поэтому появилась ВТОРАЯ, перегруженная функция, а
-- старая 2-аргументная осталась жить рядом.
--
-- Итог: при вызове с двумя аргументами (кнопка «позвать официанта» в
-- приложении гостя) PostgREST видит двух кандидатов —
--   call_waiter(uuid, text)  и  call_waiter(uuid, text, text DEFAULT NULL)
-- — не может выбрать и падает с PGRST203. Вызов не проходит, официант
-- ничего не получает.
--
-- Решение: удалить устаревшую 2-аргументную функцию. Оставшаяся
-- 3-аргументная (актуальная, с дедупом one-pending-per-table) полностью
-- её покрывает: p_reason по умолчанию NULL, поэтому вызовы и с двумя, и с
-- тремя аргументами резолвятся в неё однозначно.
--
-- Запускать из-под supabase_admin:
--   sudo docker exec -i supabase-db psql -U supabase_admin -d postgres \
--     -v ON_ERROR_STOP=1 < supabase/migrations/20260722010000_call_waiter_fix_overload.sql
-- ============================================================

BEGIN;

-- Удаляем именно 2-аргументную перегрузку. 3-аргументная остаётся нетронутой.
DROP FUNCTION IF EXISTS public.call_waiter(uuid, text);

-- Сбрасываем кэш схемы PostgREST, чтобы он сразу увидел единственного кандидата.
NOTIFY pgrst, 'reload schema';

COMMIT;
